import java.util.*;
class Solution
{
    public static void main(String args[])
    {
        Scanner s=new Scanner(System.in);
        int n=s.nextInt();
        while(n>0)
        {
            int a,b;
            a=s.nextInt();
            b=s.nextInt();
            System.out.println(a+b);
        }
    }
}